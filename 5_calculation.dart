// 05. Write a Dart program to print the sum (addition), multiply, subtract,
//divide and remainder of two numbers.

void main() {
  int num1 = 8;
  int num2 = 3;
  
  int sum = num1 + num2;
  print('Sum: $sum');
  
  int mul = num1 * num2;
  print('Multiply: $mul');
  
  int sub = num1 - num2;
  print('Subtract: $sub');
  
  double div = num1 / num2;
  print('Divide: $div');
  
  int rem = num1 % num2;
  print('Remainder: $rem');
}
